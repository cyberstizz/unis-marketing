// ── Helpers ──

export function formatMoney(n: number): string {
  if (n >= 1000) {
    return `$${n.toLocaleString("en-US", { maximumFractionDigits: 0 })}`;
  }
  return `$${n.toFixed(2)}`;
}

export function formatPercent(n: number): string {
  return `${Math.round(n * 100)}%`;
}
